public class Department {
    int dep_id;
    String dep_name;

    public int getId() {
        return dep_id;
    }

    public void setId(int new_id) {
        this.dep_id = new_id;
    }

    public String getName() {
        return dep_name;
    }

    public void setName(String new_name) {
        this.dep_name = new_name;
    }
}
