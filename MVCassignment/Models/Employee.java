public class Employee {
    int emp_id;
    String emp_name;

    public int getId() {
        return emp_id;
    }

    public void setId(int new_id) {
        this.emp_id = new_id;
    }

    public int getName() {
        return emp_name;
    }

    public void setName(int new_name) {
        this.emp_name = new_name;
    }
}
